package day4;

public class Ex03Con {

	public static void main(String[] args) {

		// 4. switch ~ case
		// switch(값) {
		// case 값1 : 실행문장;
		// }

		int month = 0;
		switch (month) {
		case 1:
			System.out.println("겨울");
			break;
		case 2:
			System.out.println("겨울");
			break;
		case 3:
			System.out.println("봄");
			break;
		default:
			System.out.println("잘못 입력하셨습니다");
		}

	}

}

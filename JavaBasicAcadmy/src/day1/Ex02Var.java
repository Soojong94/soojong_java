package day1;

public class Ex02Var {

	public static void main(String[] args) {

//		1. 변수 (Variable)
//		Alt+ 방향키 위 / 아래
//		한 줄 복사 : Ctrl + Alt + 방향키 위 / 아래

		int box = 7;	
		// 3이라는 값을 정수형 변수 box2에 대입해주세요
		int box2 = 3;
		
		
	System.out.println(box + box2); // 10
	System.out.println(box - box2); // 4
	System.out.println(box * box2); // 21
	System.out.println(box / box2); // 2
	
//	* = asterisk

	
	}

}

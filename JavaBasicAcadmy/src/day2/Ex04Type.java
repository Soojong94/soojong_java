package day2;

public class Ex04Type {

	public static void main(String[] args) {

//		3. 정수형 (Integer Type)
		
//		1)  byte
//		키워드 : byte
//		크기 : 1byte (8bit)
//		범위 : -128 ~ 0 ~ +127
		
		byte age = 21;
		
//		2) short
//		키워드 : short
//		크기 : 2byte(16bit)
//		범위 : -32,768 ~ 32,767
		
		short lunchMenuPrice = 7500;
		
//		3) int
//		키워드 : int
//		크기 : 4byte(32bit)
//		범위 : -21억 ~ +21억

		int lotto_winning_amount  = 1_631_432_063;
		
//		4) long
//		키워드 : long
//		크기 : 8byte (64bit)
//		범위 : -900경 ~ +900경
		
		long global_popular = 8_000_000_000L;
				
				
		
		
	}

}
